// For - iterar Arrays
const colors = ['blue', 'purple', 'red', 'green', 'orange', 'yellow']

for (let i = 0; i < colors.length; i++) {
   console.log(colors[i]);     
}

// While - verifica primeiro depois execulta a instrução
let i = 0 

while (i <= 10) {
  console.log(i);
  i++
}

// Do While - faz a instrução e depois verifica se precisa execultar novamente

do {
  console.log(i);
  i++
} while (i < 10)